package com.lem.variableexercise;


public class datatypeexercise {

	
	public static void main(String[] args) {
		int age =30;
		System.out.println(age);
		//double
		double plasticprice =1.99;
		System.out.println(plasticprice);
		//boolean 
		boolean isBig=true;
		System.out.println(isBig);
		//byte
		byte b=100;
		System.out.println(b);
		//long
		long l=50000000000L;
		System.out.println(l);
		//short
		short s=30000;
		System.out.println(s);
		//Float 
		float f =3.14f;
		System.out.println(f);
		
		

	}

}
